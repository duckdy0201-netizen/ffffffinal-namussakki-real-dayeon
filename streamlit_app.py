import streamlit as st

st.set_page_config(layout="wide") # 화면을 넓게 쓰기 위한 설정

st.title("🧱 6학년 수학: 쌓기나무 복습하기")
st.subheader("왼쪽의 쌓기나무 모양을 보고, 위에서 본 모양 격자에 알맞은 숫자를 써넣으세요!")

# 화면을 왼쪽(문제)과 오른쪽(입력창)으로 나눕니다.
col1, col2 = st.columns(2)

with col1:
    st.write("### 1. 다음 쌓기나무 모양을 관찰하세요.")
    
    # 이미지 파일 대신 이모지(🟦)의 높낮이를 이용해 입체 모양을 텍스트로 구현
    st.info("💡 [힌트] 아래 모양은 앞에서 바라본 입체 모양을 나타낸 것입니다.")
    
    # 교과서 65쪽 2번 문제 구조 (1행: 3, 2, 2 / 2행: 1, 3, 2)를 시각화
    st.code("""
    [ 쌓기나무 모양 입체 보기 ]
    
     (뒤쪽 줄)          (앞쪽 줄)
      3층: 🟦            3층:     🟦 
      2층: 🟦 🟦 🟦      2층:     🟦 🟦
      1층: 🟦 🟦 🟦      1층:   🟦 🟦 🟦
     ───────────        ───────────
      1열 2열 3열        1열 2열 3열
    """, language="text")
    
    st.write("📌 **뒤쪽 줄**의 1열은 3층, 2열은 2층, 3열은 2층입니다.")
    st.write("📌 **앞쪽 줄**의 1열은 1층, 2열은 3층, 3열은 2층입니다.")

with col2:
    st.write("### 2. 위에서 본 모양에 수 쓰기")
    st.write("각 자리에 알맞은 쌓기나무 개수를 선택하세요.")
    
    st.markdown("#### ─── 뒤쪽 줄 (1행) ───")
    r1_c1 = st.selectbox("뒤쪽 줄 - 1열 (왼쪽)", [0, 1, 2, 3, 4], key="r1c1")
    r1_c2 = st.selectbox("뒤쪽 줄 - 2열 (가운데)", [0, 1, 2, 3, 4], key="r1c2")
    r1_c3 = st.selectbox("뒤쪽 줄 - 3열 (오른쪽)", [0, 1, 2, 3, 4], key="r1c3")
    
    st.divider() # 구분선
    
    st.markdown("#### ─── 앞쪽 줄 (2행) ───")
    r2_c1 = st.selectbox("앞쪽 줄 - 1열 (왼쪽)", [0, 1, 2, 3, 4], key="r2c1")
    r2_c2 = st.selectbox("앞쪽 줄 - 2열 (가운데)", [0, 1, 2, 3, 4], key="r2c2")
    r2_c3 = st.selectbox("앞쪽 줄 - 3열 (오른쪽)", [0, 1, 2, 3, 4], key="r2c3")

# 하단 채점 세션
st.write("")
st.write("---")
if st.button("정답 확인하기", type="primary"):
    # 수업안에 적어두신 정답 '3 2 2 / 1 3 2'와 학생이 입력한 값이 일치하는지 비교
    if r1_c1 == 3 and r1_c2 == 2 and r1_c3 == 2 and r2_c1 == 1 and r2_c2 == 3 and r2_c3 == 2:
        st.success("🎉 정답입니다! 아주 정확하게 공간을 분석했네요!")
        st.balloons() # 화면에 풍선이 터지는 효과
    else:
        st.error("❌ 정답이 아닙니다. 각 층의 높이를 다시 한번 확인하고 재도전해 보세요! 🤔")
